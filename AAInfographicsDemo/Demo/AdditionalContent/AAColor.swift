import UIKit

public class AAColor: NSObject {

    public static var black: String! {
        get {
            return "black"
        }
    } // 0.0 white
    
   public static var darkGray: String! {
        get {
            return "darkGray"
        }
    } // 0.333 white
    
   public static var lightGray: String! {
        get {
            return "lightGray"
        }
    } // 0.667 white
    
   public static var white: String! {
        get {
            return "white"
        }
    } // 1.0 white
    
   public static var gray: String! {
        get {
            return "gray"
        }
    } // 0.5 white
    
   public static var red: String! {
        get {
            return "red"
        }
    } // 1.0, 0.0, 0.0 RGB
    
   public static var green: String! {
        get {
            return "green"
        }
    } // 0.0, 1.0, 0.0 RGB
    
   public static var blue: String! {
        get {
            return "blue"
        }
    } // 0.0, 0.0, 1.0 RGB
    
   public static var cyan: String! {
        get {
            return "cyan"
        }
    } // 0.0, 1.0, 1.0 RGB
    
   public static var yellow: String! {
        get {
            return "yellow"
        }
    } // 1.0, 1.0, 0.0 RGB
    
   public static var magenta: String! {
        get {
            return "magenta"
        }
    } // 1.0, 0.0, 1.0 RGB
    
   public static var orange: String! {
        get {
            return "orange"
        }
    } // 1.0, 0.5, 0.0 RGB
    
   public static var purple: String! {
        get {
            return "purple"
        }
    } // 0.5, 0.0, 0.5 RGB
    
   public static var brown: String! {
        get {
            return "brown"
        }
    } // 0.6, 0.4, 0.2 RGB
    
   public static var clear: String! {
        get {
            return "#00000000"
        }
    } // 0.0 white, 0.0 alpha
}
